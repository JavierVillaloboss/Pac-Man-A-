def dibujar_mapa(ventana):
    for y, fila in enumerate(mapa):
        for x, valor in enumerate(fila):
            # Si es un espacio vacío (0), lo dibujamos como negro
            if valor == 0:
                color = NEGRO
                pygame.draw.rect(ventana, color, (x * TAM_CELDA, y * TAM_CELDA, TAM_CELDA, TAM_CELDA))
            # Si es una pared (1), la dibujamos en azul
            elif valor == 1:
                color = AZUL
                pygame.draw.rect(ventana, color, (x * TAM_CELDA, y * TAM_CELDA, TAM_CELDA, TAM_CELDA))
            # Si es una píldora (2), dibujamos el camino (negro) y luego la píldora (verde)
            elif valor == 2:
                # Primero dibujamos el camino negro
                pygame.draw.rect(ventana, NEGRO, (x * TAM_CELDA, y * TAM_CELDA, TAM_CELDA, TAM_CELDA))
                # Luego dibujamos la píldora verde encima del camino
                pygame.draw.circle(ventana, VERDE, (x * TAM_CELDA + TAM_CELDA // 2, y * TAM_CELDA + TAM_CELDA // 2), 5)